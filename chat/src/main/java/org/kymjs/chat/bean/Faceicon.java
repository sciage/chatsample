package org.kymjs.chat.bean;

import java.io.Serializable;

/**
 * expression javabean
 * @author kymjs (http://www.kymjs.com/) on 6/9/15.
 */
public class Faceicon implements Serializable {
    private String name; //The value in the network transmission
    private String path; //The absolute path in the system
    private String fileName; //Image file name

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
}
